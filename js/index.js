window.onload = function() {
    const cookies = document.cookie.split('; ').reduce((acc, cookie) => {
        const [name, value] = cookie.split('=');
        acc[name] = value;
        return acc;
    }, {});

    if (!cookies.session) {
        // Redirecciona al login si no hay una sesi√≥n activa
        window.location.href = 'login.html';
    }
};